// button hover
ScrollTrigger.matchMedia({

  "(min-width: 1024px)": function () {
    const deleteButton = document.querySelectorAll(".d__b__button");

    deleteButton.forEach((button) => {
      const letters = Array.from(button.querySelectorAll(".button__fade"));

      button.addEventListener("mouseenter", () => {
        const tl = gsap.timeline();
        tl.to(letters, {
          opacity: 0,
          x: 15,
          filter: "blur(5px)",
          duration: 0.9,
          ease: "power2.out",
          stagger: 0.05,
        });
      });

      button.addEventListener("mouseleave", () => {
        const tl = gsap.timeline();
        tl.to(letters, {
          opacity: 1,
          x: 0,
          filter: "blur(0px)",
          duration: 0.9,
          ease: "power2.out",
          stagger: 0.05,
        });
      });
    });
  }
});


//page load anim
window.addEventListener("DOMContentLoaded", () => {
  const titleLetters = document.querySelector(".title__letter");
  const buttonLetters = document.querySelectorAll(".button__fade");
  const dangerText = document.querySelector(".danger__text");

  gsap.to(titleLetters, {
    opacity: 1,
    x: 0,
    filter: "blur(0px)",
    duration: 1,
    ease: "power2.out",
    stagger: 0.09,
    delay: 0.1
  });

  gsap.to(buttonLetters, {
    opacity: 1,
    x: 0,
    filter: "blur(0px)",
    duration: 1.2,
    ease: "power2.out",
    stagger: 0.01,
    delay: 0.3
  });

  if (dangerText) {
   gsap.to(dangerText, {
      opacity: 1,
      x: 0,
      filter: "blur(0px)",
      duration: 1.2,
      ease: "power2.out",
      stagger: 0.01,
      delay: 0.3
    });
  }
});
